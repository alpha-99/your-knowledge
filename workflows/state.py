"""
工作流共享状态定义 — KBState

LangGraph 状态图的核心数据结构。每个节点读取 State、返回部分更新（dict），
LangGraph 自动将返回值合并到全局 State 中。

遵循"报告式通信"原则：State 字段是各节点的结构化摘要输出，而非原始数据。
"""

from typing import TypedDict


class KBState(TypedDict):
    """知识库工作流的全局共享状态

    LangGraph 节点通过 State（只读）获取上游输出，
    返回 dict（部分更新）写入下游可见的新值。
    """

    # ------------------------------------------------------------------
    # 采集节点输出 — 从各数据源抓取的原始记录
    # 每项格式: {source, title, url, description, stars, language, collected_at}
    # 包含正常数据和采集失败的 [ERROR] 标记条目
    # ------------------------------------------------------------------
    sources: list[dict]

    # ------------------------------------------------------------------
    # 分析节点输出 — LLM 分析后的结构化报告
    # 在 sources 字段基础上追加: summary, tags, relevance_score, category, key_insight
    # 错误条目保留原始字段，summary 包含错误信息
    # ------------------------------------------------------------------
    analyses: list[dict]

    # ------------------------------------------------------------------
    # 整理节点输出 — 去重、过滤、格式化后的知识条目
    # 标准格式: {id, title, source, url, collected_at, summary, tags,
    #            relevance_score, category, key_insight}
    # relevance_score < 0.6 的条目已被过滤，URL 已去重
    # ------------------------------------------------------------------
    articles: list[dict]

    # ------------------------------------------------------------------
    # 审核节点输出 — 质量审核员对当前批次的具体反馈
    # 通过时为空或包含肯定评语；未通过时包含维度的改进建议
    # 整理节点会读取此字段进行修正
    # ------------------------------------------------------------------
    review_feedback: str

    # ------------------------------------------------------------------
    # 审核节点输出 — 当前批次是否达到质量门槛（overall_score >= 3.5）
    # True → 进入 save 节点；False → 回到 organize 节点（最多重试 3 次）
    # ------------------------------------------------------------------
    review_passed: bool

    # ------------------------------------------------------------------
    # 审核节点输出 — 当前审核循环次数计数器
    # 初始值为 0，每次审核 +1；达到 3 时 review_node 强制通过
    # ------------------------------------------------------------------
    iteration: int

    needs_human_review: bool # ← 新增：HumanFlag 节点设为 True

    # ------------------------------------------------------------------
    # 全局累加 — LLM 调用 token 用量与成本追踪
    # 格式: {prompt_tokens, completion_tokens, total_cost_yuan}
    # 各节点调用 chat/chat_json 后通过 accumulate_usage 累加
    # ------------------------------------------------------------------
    cost_tracker: dict
