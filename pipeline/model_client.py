def _build_config(provider_name: str, defaults: dict) -> dict:
    """Build provider config by reading from env vars, falling back to defaults.

    Looks for ``{PROVIDER}_API_BASE``, ``{PROVIDER}_DEFAULT_MODEL``,
    ``{PROVIDER}_PRICING_INPUT``, and ``{PROVIDER}_PRICING_OUTPUT`` in the
    environment.  Falls back to the hard-coded defaults when an env var is
    absent or unparseable.

    Args:
        provider_name: Lowercase provider key (``deepseek``, ``qwen``, ``openai``).
        defaults: The hard-coded fallback values.

    Returns:
        A config dict with keys ``api_base``, ``api_key_env``, ``default_model``,
        and ``pricing`` (``{input, output}``).
    """
    prefix = provider_name.upper()

    api_base = os.getenv(f"{prefix}_API_BASE", defaults["api_base"])
    default_model = os.getenv(f"{prefix}_DEFAULT_MODEL", defaults["default_model"])

    try:
        pricing_input = float(
            os.getenv(f"{prefix}_PRICING_INPUT", str(defaults["pricing_input"]))
        )
    except ValueError:
        pricing_input = defaults["pricing_input"]
    try:
        pricing_output = float(
            os.getenv(f"{prefix}_PRICING_OUTPUT", str(defaults["pricing_output"]))
        )
    except ValueError:
        pricing_output = defaults["pricing_output"]

    return {
        "api_base": api_base,
        "api_key_env": defaults["api_key_env"],
        "default_model": default_model,
        "pricing": {"input": pricing_input, "output": pricing_output},
    }


_PROVIDER_CONFIGS: dict[str, dict] = {
    name: _build_config(name, defaults)
    for name, defaults in _DEFAULTS.items()
}

MAX_RETRIES: int = 3
"""Maximum number of retry attempts for API calls."""

RETRY_BACKOFF_BASE: float = 2.0
"""Base multiplier (seconds) for exponential backoff."""

REQUEST_TIMEOUT: float = 60.0
"""HTTP request timeout in seconds."""

CHARS_PER_TOKEN_ENGLISH: int = 4
"""Approximate characters per token for alphabetic / ASCII text."""

CHARS_PER_TOKEN_CHINESE: int = 2
"""Approximate characters per token for CJK text."""

_SUPPORTED_PROVIDERS: str = ", ".join(sorted(_PROVIDER_CONFIGS.keys()))

# =====================================================================
# Data Classes
# =====================================================================


@dataclass
class Usage:
    """Token usage statistics returned from an LLM API call.

    Attributes:
        prompt_tokens: Number of tokens consumed by the input / prompt.
        completion_tokens: Number of tokens generated by the model.
        total_tokens: Sum of prompt and completion tokens.
    """

    prompt_tokens: int = 0
    completion_tokens: int = 0
    total_tokens: int = 0


@dataclass
class LLMResponse:
    """Unified response from an LLM API call.

    Attributes:
        content: The text content returned by the model.
        usage: Token usage statistics for the request.
    """

    content: str
    usage: Usage

    def __repr__(self) -> str:
        preview = self.content[:80].replace("\n", " ")
        suffix = "..." if len(self.content) > 80 else ""
        return (
            f"LLMResponse(content='{preview}{suffix}', "
            f"usage={self.usage})"
        )


# =====================================================================
# Abstract Base Class
# =====================================================================


class LLMProvider(ABC):
    """Abstract base class defining the interface for LLM providers.

    All provider implementations must subclass this and implement the
    ``chat`` method.
    """

    @abstractmethod
    def chat(self, messages: list[dict[str, str]], **kwargs) -> LLMResponse:
        """Send a chat completion request.

        Args:
            messages: A list of message dicts with ``role`` and ``content``
                keys (e.g. ``{"role": "user", "content": "Hello"}``).
            **kwargs: Additional parameters forwarded to the API
                (temperature, max_tokens, top_p, etc.).

        Returns:
            An :class:`LLMResponse` containing the model reply and token usage.

        Raises:
            httpx.HTTPError: On network or HTTP-level failures.
        """
        ...

    @property
    @abstractmethod
    def model(self) -> str:
        """Return the model identifier string used by this provider."""
        ...


# =====================================================================
# OpenAI-Compatible Provider
# =====================================================================


class OpenAICompatibleProvider(LLMProvider):
    """Generic provider for OpenAI-compatible chat completion endpoints.

    Works with any service that exposes a ``/chat/completions`` endpoint
    matching the OpenAI v1 API schema.

    Args:
        api_base: Base URL of the API (e.g. ``https://api.openai.com/v1``).
        api_key: Bearer token for authentication.
        model: Model identifier string.
    """

    def __init__(self, api_base: str, api_key: str, model: str) -> None:
        self._api_base = api_base.rstrip("/")
        self._api_key = api_key
        self._model = model

    @property
    def model(self) -> str:
        return self._model

    def chat(
        self, messages: list[dict[str, str]], **kwargs
    ) -> LLMResponse:
        """Send a chat completion request to the provider.

        Args:
            messages: A list of message dicts with ``role`` and ``content``.
            **kwargs: Additional API parameters (temperature, max_tokens, etc.).

        Returns:
            Parsed :class:`LLMResponse`.

        Raises:
            httpx.HTTPStatusError: If the server returns a non-2xx status.
            httpx.RequestError: On connection / timeout errors.
        """
        url = f"{self._api_base}/chat/completions"
        payload: dict = {
            "model": self._model,
            "messages": messages,
            **kwargs,
        }
        headers = {
            "Authorization": f"Bearer {self._api_key}",
            "Content-Type": "application/json",
        }

        logger.debug("POST %s with model=%s", url, self._model)
        try:
            response = httpx.post(
                url,
                json=payload,
                headers=headers,
                timeout=REQUEST_TIMEOUT,
            )
            response.raise_for_status()
        except httpx.HTTPStatusError:
            logger.error(
                "HTTP %d from %s: %s",
                response.status_code,
                self._model,
                response.text[:500],
            )
            raise
        except httpx.RequestError:
            logger.exception("Request failed for %s", self._model)
            raise

        data = response.json()
        choice = data["choices"][0]
        message = choice["message"]
        usage_raw = data.get("usage", {})

        usage = Usage(
            prompt_tokens=usage_raw.get("prompt_tokens", 0),
            completion_tokens=usage_raw.get("completion_tokens", 0),
            total_tokens=usage_raw.get("total_tokens", 0),
        )

        return LLMResponse(content=message.get("content", ""), usage=usage)


