# Provider Factory
# =====================================================================


def create_provider(
    provider_name: Optional[str] = None,
    model: Optional[str] = None,
) -> OpenAICompatibleProvider:
    """Create a configured provider instance.

    Reads the API key from the appropriate environment variable and
    returns a ready-to-use :class:`OpenAICompatibleProvider`.

    Args:
        provider_name: One of ``deepseek``, ``qwen``, ``openai``.
            Defaults to the ``LLM_PROVIDER`` environment variable or
            ``deepseek``.
        model: Override the default model for the selected provider.

    Returns:
        A configured provider instance.

    Raises:
        ValueError: If the provider name is unknown or the required
            API key environment variable is not set.
    """
    name = (provider_name or _DEFAULT_PROVIDER).lower()
    if name not in _PROVIDER_CONFIGS:
        raise ValueError(
            f"Unknown provider '{name}'. "
            f"Supported: {_SUPPORTED_PROVIDERS}"
        )

    config = _PROVIDER_CONFIGS[name]
    api_key = os.getenv(config["api_key_env"])
    if not api_key:
        raise ValueError(
            f"Environment variable '{config['api_key_env']}' is not set. "
            f"Cannot authenticate with {name}."
        )

    selected_model = model or config["default_model"]
    logger.info("Creating provider '%s' with model '%s'", name, selected_model)
    return OpenAICompatibleProvider(
        api_base=config["api_base"],
        api_key=api_key,
        model=selected_model,
    )


def list_providers() -> list[str]:
    """Return a list of supported provider names."""
    return sorted(_PROVIDER_CONFIGS.keys())


def get_provider_pricing(
    provider_name: Optional[str] = None,
) -> dict[str, float]:
    """Return the per-million-token pricing dict for a provider.

    Args:
        provider_name: Provider name. Defaults to current ``LLM_PROVIDER``.

    Returns:
        Dict with ``input`` and ``output`` keys in USD per 1M tokens.
    """
    name = (provider_name or _DEFAULT_PROVIDER).lower()
    return dict(_PROVIDER_CONFIGS[name]["pricing"])


# =====================================================================
# Retry Logic
# =====================================================================


def _should_retry(status_code: int) -> bool:
    """Decide whether an HTTP response status warrants a retry.

    Retries are attempted on server errors (5xx) and rate-limiting (429).
    Client errors (4xx) other than 429 are considered permanent and are
    not retried.

    Args:
        status_code: HTTP response status code.

    Returns:
        ``True`` if the request should be retried.
    """
    return status_code >= 500 or status_code == 429


def chat_with_retry(
    messages: list[dict[str, str]],
    provider: Optional[LLMProvider] = None,
    max_retries: int = MAX_RETRIES,
    **kwargs,
) -> LLMResponse:
    """Send a chat request with automatic retry on transient failures.

    Uses exponential backoff starting from :data:`RETRY_BACKOFF_BASE`
    seconds.  Only server errors (5xx) and rate-limiting (429) are retried;
    other client errors (4xx) are raised immediately.

    Args:
        messages: A list of message dicts with ``role`` and ``content``.
        provider: An existing provider instance.  If ``None`` a default
            provider is created via :func:`create_provider`.
        max_retries: Maximum number of retries (default 3).
        **kwargs: Additional parameters forwarded to :meth:`LLMProvider.chat`.

    Returns:
        :class:`LLMResponse` from the successful call.

    Raises:
        httpx.HTTPStatusError: If all retries are exhausted or a
            non-retryable error occurs.
        httpx.RequestError: On persistent network failures.
    """
    p = provider or create_provider()
    last_error: Optional[Exception] = None

    for attempt in range(max_retries + 1):
        try:
            return p.chat(messages, **kwargs)
        except httpx.HTTPStatusError as exc:
            last_error = exc
            if not _should_retry(exc.response.status_code):
                logger.error(
                    "Non-retryable HTTP %d, not retrying.",
                    exc.response.status_code,
                )
                raise
        except httpx.RequestError as exc:
            last_error = exc

        if attempt < max_retries:
            delay = RETRY_BACKOFF_BASE ** (attempt + 1)
            logger.warning(
                "Attempt %d/%d failed: %s. Retrying in %.1fs ...",
                attempt + 1,
                max_retries + 1,
                last_error,
                delay,
            )
            time.sleep(delay)

    logger.error("All %d attempts exhausted.", max_retries + 1)
    raise last_error  # type: ignore[misc]


# =====================================================================
# Token Estimation & Cost
# =====================================================================


def estimate_tokens(text: str) -> int:
    """Estimate the number of tokens for a given text string.

    Uses a heuristic: ~4 characters per token for ASCII text and
    ~2 characters per token for CJK characters.  This is a rough
    approximation and may vary across tokenizers.

    Args:
        text: The input text to estimate.

    Returns:
        Estimated token count (minimum 1).
    """
    cjk = sum(1 for ch in text if "\u4e00" <= ch <= "\u9fff")
    ascii_chars = len(text) - cjk
    estimated = int(
        cjk / CHARS_PER_TOKEN_CHINESE + ascii_chars / CHARS_PER_TOKEN_ENGLISH
    )
    return max(estimated, 1)


def calculate_cost(
    prompt_tokens: int,
    completion_tokens: int,
    provider_name: Optional[str] = None,
) -> float:
    """Calculate the estimated USD cost of an API call.

    Uses per-million-token pricing from the provider configuration.

    Args:
        prompt_tokens: Number of input / prompt tokens.
        completion_tokens: Number of output / completion tokens.
        provider_name: Provider name.  Defaults to current ``LLM_PROVIDER``.

    Returns:
        Estimated cost in US dollars.
    """
    pricing = get_provider_pricing(provider_name)
    input_cost = (prompt_tokens / 1_000_000) * pricing["input"]
    output_cost = (completion_tokens / 1_000_000) * pricing["output"]
    return input_cost + output_cost


# =====================================================================
# Convenience Function
# =====================================================================


def quick_chat(
    prompt: str,
    system: Optional[str] = None,
    provider_name: Optional[str] = None,
    model: Optional[str] = None,
    **kwargs,
) -> LLMResponse:
    """Send a one-shot chat request – the simplest way to query an LLM.

    Constructs a message list from the given prompt and optional system
    instruction, then delegates to :func:`chat_with_retry`.

    Args:
        prompt: The user message to send.
        system: Optional system-level instruction (role ``system``).
        provider_name: Provider to use (defaults to ``LLM_PROVIDER`` env).
        model: Model override for the selected provider.
        **kwargs: Additional parameters forwarded to the chat API
            (temperature, max_tokens, etc.).

    Returns:
        :class:`LLMResponse` containing the model's reply.

    Example::

        >>> response = quick_chat("Hello, who are you?")
        >>> print(response.content)

        >>> response = quick_chat(
        ...     "Summarize this article.",
        ...     system="You are a helpful assistant.",
        ...     provider_name="openai",
        ...     temperature=0.3,
        ... )
    """
    messages: list[dict[str, str]] = []
    if system:
        messages.append({"role": "system", "content": system})
    messages.append({"role": "user", "content": prompt})

    provider = create_provider(provider_name=provider_name, model=model)
    return chat_with_retry(messages, provider=provider, **kwargs)


