#!/usr/bin/env python3
"""Unified LLM client supporting DeepSeek, Qwen, and OpenAI providers.

Provides a common interface for OpenAI-compatible API endpoints with
built-in retry logic, token estimation, and cost calculation.

Configuration is loaded from ``.env`` via python-dotenv.  Copy
``.env.example`` to ``.env`` and fill in your API keys.  All settings
have sensible defaults so the module works even without a ``.env`` file.

Key environment variables::

    LLM_PROVIDER          - provider: deepseek (default), qwen, openai
    {PROVIDER}_API_KEY    - API key (DEEPSEEK_API_KEY, DASHSCOPE_API_KEY, OPENAI_API_KEY)
    {PROVIDER}_API_BASE   - override the default base URL
    {PROVIDER}_DEFAULT_MODEL - override the default model name
    {PROVIDER}_PRICING_INPUT  - USD per 1M input tokens
    {PROVIDER}_PRICING_OUTPUT - USD per 1M output tokens

Usage:
    from pipeline.model_client import quick_chat

    response = quick_chat("Explain what a neural network is.")
    print(response.content)
"""

import logging
import os
import time
from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Optional

import httpx
from dotenv import load_dotenv

load_dotenv()

logger = logging.getLogger(__name__)

